

namespace EjercicioExcepciones
{
    public class Operaciones
    {
        double n1;
        double n2;

        public double Suma()
        {
            return n1 + n2;
        }

        public double Resta()
        {
            return n1-n2;
        }

        public double Division()
        {
            return n1/n2;
        }

        public double Multiplicacion()
        {
            return n1*n2;
        }
    }
}
